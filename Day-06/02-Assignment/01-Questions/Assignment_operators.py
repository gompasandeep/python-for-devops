# Step 1: Create a variable total and initialize it to 10
total = 10

# Step 2: Use assignment operators to update the value of total
total += 5  # Equivalent to: total = total + 5
total -= 3  # Equivalent to: total = total - 3
total *= 2  # Equivalent to: total = total * 2
total /= 4  # Equivalent to: total = total / 4

# Step 3: Print the final value of total
print("Final value of total:", total)
