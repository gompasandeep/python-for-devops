# Given values of a and b
a = 20
b = 4

# Compare using comparison operators
less_than = a < b
greater_than = a > b
less_than_or_equal = a <= b
greater_than_or_equal = a >= b
equal_to = a == b
not_equal_to = a != b

# Print the results of each comparison
print("Is a less than b?", less_than)
print("Is a greater than b?", greater_than)
print("Is a less than or equal to b?", less_than_or_equal)
print("Is a greater than or equal to b?", greater_than_or_equal)
print("Is a equal to b?", equal_to)
print("Is a not equal to b?", not_equal_to)
