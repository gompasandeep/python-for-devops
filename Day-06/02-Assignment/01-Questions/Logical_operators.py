logical_or = x or y
logical_not_x = not x
logical_not_y = not y

# Step 3: Print the results of each logical operation
print("Logical AND (x and y):", logical_and)
print("Logical OR (x or y):", logical_or)
print("Logical NOT (not x):", logical_not_x)
print("Logical NOT (not y):", logical_not_y)
